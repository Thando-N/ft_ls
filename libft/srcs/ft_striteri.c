#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	int		cpt;

	cpt = 0;
	if (s != NULL && f != NULL)
	{
		while (s[cpt])
		{
			f(cpt, &s[cpt]);
			cpt++;
		}
	}
}
